            f"üëÅ –£–∂–µ –ø—Ä–æ—Å–º–æ—Ç—Ä–µ–Ω–Ω—ã—Ö: {stats['seen_count']}\n\n"
            f"üïê –í—Ä–µ–º—è –ø–æ–∏—Å–∫–∞: {stats['search_time']}\n"
            f"üìç –†–µ–≥–∏–æ–Ω: –ü–µ—Ä–º—å\n"
            f"üìÖ –ü–µ—Ä–∏–æ–¥: –ü–æ—Å–ª–µ–¥–Ω–∏–π –º–µ—Å—è—Ü\n\n"
